colors = {
    "красный": (255, 0, 0),
    "зеленый": (0, 255, 0),
    "синий": (0, 0, 255),
    "желтый": (255, 255, 0),
    "фиолетовый": (128, 0, 128),
    "белый": (255, 255, 255),
    "черный": (0, 0, 0)
}
red = colors["красный"]
blue = colors["синий"]
mixed = ((red[0] + blue[0]) // 2, (red[1] + blue[1]) // 2, (red[2] + blue[2]) // 2)
print(f"Смешанный цвет (красный + синий): {mixed}")
green = colors["зеленый"]
inverted = (255 - green[0], 255 - green[1], 255 - green[2])
print(f"Инвертированный зеленый: {inverted}")