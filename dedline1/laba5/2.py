input_str = input("Введите три целых числа через пробел (пример 10 3 2): ")
a, b, c, = map(int, input_str.split())
print(f"{a} * {b} = {a*b}")
print(f"{b} * {c} = {b*c}")
print(f"{c} * {a} = {c*a}")
print(f"{a} ** 4 = {a**4}")
print(f"{b} % {c} = {b%c}")
print(f"{c} // {a} = {c//a}")
print(f"Сумма = {a**4 + b%c + c//a}")
